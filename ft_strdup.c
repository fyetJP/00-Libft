#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*dst;

	if (s == ((void *)0))
		return ((char *)NULL);
	i = 0;
	while (s[i] != '\0')
		i++;
	if (!(dst = (char *)malloc((i + 1) * sizeof(char))))
		return ((char *)NULL);
	i = 0;
	while (s[i] != '\0')
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}